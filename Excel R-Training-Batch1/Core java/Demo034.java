public class Demo034 {
    public static void main(String[] args) {
        int x = 9;
        if (x % 2 == 0) {
            System.out.println("x is even");
        }
        if (x == 0) {
            System.out.println("x is zero");
        } else {
            System.out.println("x is odd");
        }
    }
}